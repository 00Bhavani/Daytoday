package com.in.datatypes;
class ByteExample {
    public static void main(String[] args) {
        byte age = 25;
        System.out.println("Byte value: " + age);
    }
}

class ShortExample {
    public static void main(String[] args) {
        short temperature = -150;
        System.out.println("Short value: " + temperature);
    }
}

class IntExample {
    public static void main(String[] args) {
        int population = 1500000;
        System.out.println("Int value: " + population);
    }
}

class LongExample {
    public static void main(String[] args) {
        long distanceToSun = 15000000000L;
        System.out.println("Long value: " + distanceToSun);
    }
}

class FloatExample {
    public static void main(String[] args) {
        float pi = 3.14f;
        System.out.println("Float value: " + pi);
    }
}

class DoubleExample {
    public static void main(String[] args) {
        double gravity = 9.80665;
        System.out.println("Double value: " + gravity);
    }
}

class BooleanExample {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        System.out.println("Boolean value: " + isJavaFun);
    }
}

class CharExample {
    public static void main(String[] args) {
        char grade = 'A';
        System.out.println("Char value: " + grade);
    }
}

public class Datatypes_prog {
    public static void main(String[] args) {
        byte smallNumber = 100;
        short mediumNumber = 30000;
        int integerNumber = 100000;
        long largeNumber = 10000000000L;  // Note 'L' suffix
        float decimalNumber = 10.5f;      // Note 'f' suffix
        double preciseDecimal = 20.99;
        boolean isJavaFun = true;
        char grade = 'A';

        System.out.println("byte value: " + smallNumber);
        System.out.println("short value: " + mediumNumber);
        System.out.println("int value: " + integerNumber);
        System.out.println("long value: " + largeNumber);
        System.out.println("float value: " + decimalNumber);
        System.out.println("double value: " + preciseDecimal);
        System.out.println("boolean value: " + isJavaFun);
        System.out.println("char value: " + grade);
    }
}
